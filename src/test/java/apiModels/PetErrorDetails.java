package apiModels;

public class PetErrorDetails {
		int Code;
	 	String Type;
		String Message;
		
		public int getCode() {
			return Code;
		}
		public void setCode(int code) {
			Code = code;
		}
		public String getType() {
			return Type;
		}
		public void setType(String type) {
			Type = type;
		}
		public String getMessage() {
			return Message;
		}
		public void setMessage(String message) {
			Message = message;
		}
	 
	 
}
